#ifndef IO_H
#define IO_H

#include <cstdio>
#include <cstdarg>

class IO
	{
	public:
		/*********************************************************************\
		|* Constructor
		\*********************************************************************/
		explicit IO(void);

		/*********************************************************************\
		|* Write a character
		\*********************************************************************/
		virtual void putchar(int c);

		/*********************************************************************\
		|* Read a character
		\*********************************************************************/
		virtual int getchar(void);

		/*********************************************************************\
		|* Peek at a character
		\*********************************************************************/
		virtual int peekchar(void);

		/*********************************************************************\
		|* printf
		\*********************************************************************/
		virtual int printf(const char *format, ...);
	};

#endif // IO_H
